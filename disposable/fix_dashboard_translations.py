def flatten_nested(data):
    """Convert nested structure to flat dashboard.key structure."""
    if "dashboard" in data and isinstance(data["dashboard"], dict):
        flat = {}
        for key, value in data["dashboard"].items():
            flat[f"dashboard.{key}"] = value
        return flat
    return data


def unflatten(data):
    """Convert flat dashboard.key structure back to nested (not used for output, just for reference)."""
    nested = {"dashboard": {}}
    for key, value in data.items():
        if key.startswith("dashboard."):
            nested["dashboard"][key[10:]] = value
        else:
            nested[key] = value
    return nested


def process_language(lang_code, file_path):
    """Process a single language file."""
    with open(file_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    # Check if nested and convert to flat
    is_nested = "dashboard" in data and isinstance(data.get("dashboard"), dict)
    if is_nested:
        data = flatten_nested(data)
    
    # Fill in missing/untranslated keys
    for key in en_keys:
        if key not in data or data[key] == en_values[key]:
            # Try to get translation
            if key in TRANSLATIONS and lang_code in TRANSLATIONS[key]:
                data[key] = TRANSLATIONS[key][lang_code]
            # Otherwise keep English (already matches en_values)
    
    return data, is_nested


def write_flat_json(data, file_path):
    """Write JSON in flat format with 2-space indentation, sorted keys."""
    with open(file_path, "w", encoding="utf-8") as f:
        f.write("{\n")
        keys = sorted(data.keys())
        for i, key in enumerate(keys):
            comma = "," if i < len(keys) - 1 else ""
            f.write(f'  "{key}": {json.dumps(data[key], ensure_ascii=False)}{comma}\n')
        f.write("}\n")


