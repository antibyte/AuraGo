foreach ($lang in $translations.Keys) {
    $filePath = Join-Path $langDir "$lang.json"
    if (-not (Test-Path $filePath)) {
        Write-Warning "File not found: $filePath"
        continue
    }

    $json = Get-Content $filePath -Raw -Encoding UTF8 | ConvertFrom-Json
    
    $trans = $translations[$lang]
    foreach ($key in $trans.Keys) {
        $fullKey = "knowledge.$key"
        # Add or update the property
        $json | Add-Member -NotePropertyName $fullKey -NotePropertyValue $trans[$key] -Force
    }

    # Convert back to JSON with proper formatting
    $jsonStr = $json | ConvertTo-Json -Depth 10
    
    # Write back
    [System.IO.File]::WriteAllText($filePath, $jsonStr, [System.Text.Encoding]::UTF8)
    Write-Host "Updated $lang.json"
}

Write-Host "`nAll files updated successfully!"
