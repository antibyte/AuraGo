func TestTryClaimWeeklyReflectionSingleFlight(t *testing.T) {
	releaseWeeklyReflectionClaim()

	if !tryClaimWeeklyReflection(nil) {
		t.Fatal("expected first weekly reflection claim to succeed")
	}
	if tryClaimWeeklyReflection(nil) {
		t.Fatal("expected second weekly reflection claim to be rejected")
	}

	releaseWeeklyReflectionClaim()
	if !tryClaimWeeklyReflection(nil) {
		t.Fatal("expected claim to succeed after release")
	}
}

