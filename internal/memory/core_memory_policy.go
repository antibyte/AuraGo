func hasAnyCoreMemoryMarker(text string, markers []string) bool {
	for _, marker := range markers {
		if strings.Contains(text, marker) {
			return true
		}
	}
	return false
}

func isCoreMemoryOperationalSnapshot(text string) bool {
	if hasAnyCoreMemoryMarker(text, transientCoreMemoryOperationalMarkers) {
		return true
	}
	if coreMemoryOperationalID.MatchString(text) || coreMemoryHTTPStatus.MatchString(text) {
		return true
	}
	return coreMemoryOperationalDate.MatchString(text) &&
		hasAnyCoreMemoryMarker(text, transientCoreMemoryOperationalContextMarkers)
}

func isDurableCoreMemoryNetworkFact(text string) bool {
	return hasAnyCoreMemoryMarker(text, durableCoreMemoryNetworkMarkers)
}

func isCoreMemoryNetworkDiscovery(text string) bool {
	if !coreMemoryIPv4.MatchString(text) {
		return false
	}
	if isDurableCoreMemoryNetworkFact(text) {
		return false
	}
	if hasAnyCoreMemoryMarker(text, coreMemoryNetworkDiscoveryStrongMarkers) {
		return true
	}
	if hasAnyCoreMemoryMarker(text, coreMemoryNetworkDiscoveryVerbMarkers) {
		return true
	}
	if strings.Contains(text, "port ") &&
		(hasAnyCoreMemoryMarker(text, coreMemoryNetworkDiscoveryVerbMarkers) ||
			hasAnyCoreMemoryMarker(text, coreMemoryNetworkDiscoveryStrongMarkers)) {
		return true
	}
	if hasAnyCoreMemoryMarker(text, transientCoreMemoryNetworkDiscoveryContextMarkers) &&
		hasAnyCoreMemoryMarker(text, coreMemoryNetworkDiscoveryVerbMarkers) {
		return true
	}
	return false
}

func isCoreMemoryToolStateClaim(text string) bool {
	hasToolSubject := strings.Contains(text, "tool") ||
		strings.Contains(text, "integration") ||
		strings.Contains(text, "virtual_desktop") ||
		strings.Contains(text, "native function") ||
		strings.Contains(text, "provider-invalid") ||
		strings.Contains(text, "send_notification") ||
		strings.Contains(text, "ntfy") ||
		strings.Contains(text, "telegram") ||
		strings.Contains(text, "discord") ||
		strings.Contains(text, "pushover") ||
		strings.Contains(text, "rocketchat") ||
		strings.Contains(text, "ddg_search")
	if !hasToolSubject {
		return false
	}
	stateTerms := []string{
		"failed", "failing", "failure", "error", "unavailable", "not available",
		"unsupported", "invalid", "disabled", "enabled", "configured",
		"fehlgeschlagen", "fehler", "kaputt", "nicht verfügbar", "deaktiviert",
		"aktiviert", "konfiguriert", "verfügbar", "reachable", "immer aktiv",
	}
	return hasAnyCoreMemoryMarker(text, stateTerms)
}

func isCoreMemoryInstructionLike(text string) bool {
	if strings.HasPrefix(strings.TrimSpace(text), "# system") {
		return true
	}
	if hasAnyCoreMemoryMarker(text, coreMemoryInstructionMarkers) ||
		hasAnyCoreMemoryMarker(text, coreMemoryValidationBypassMarkers) ||
		hasAnyCoreMemoryMarker(text, coreMemoryPermissionOverrideMarkers) {
		return true
	}
	if (strings.Contains(text, "always call") || strings.Contains(text, "must call")) &&
		(strings.Contains(text, "tool") ||
			strings.Contains(text, "shell") ||
			strings.Contains(text, "send_") ||
			strings.Contains(text, "discord") ||
			strings.Contains(text, "telegram")) {
		return true
	}
	if strings.Contains(text, "policy") &&
		(strings.Contains(text, "replace") || strings.Contains(text, "replaced") || strings.Contains(text, "override")) {
		return true
	}
	return false
}

type CoreMemoryReviewIssue struct {
	Index  int    `json:"index"`
	Fact   string `json:"fact"`
	Reason string `json:"reason"`
}

// ReviewCoreMemoryFacts identifies existing core-memory entries that should be
// manually cleaned up. It is report-only: callers must not delete or move data
// automatically based on this classification.
func ReviewCoreMemoryFacts(facts []string) []CoreMemoryReviewIssue {
	issues := make([]CoreMemoryReviewIssue, 0)
	for i, fact := range facts {
		if err := ValidateCoreMemoryFact(fact); err != nil {
			issues = append(issues, CoreMemoryReviewIssue{
				Index:  i,
				Fact:   fact,
				Reason: err.Error(),
			})
		}
	}
	return issues
}
