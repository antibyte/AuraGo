// MarkEpisodicMemoriesHierarchy updates the hierarchy level for the given episodic IDs.
func (s *SQLiteMemory) MarkEpisodicMemoriesHierarchy(ids []int64, newLevel int) error {
	if len(ids) == 0 {
		return nil
	}
	if newLevel <= 0 {
		newLevel = 1
	}
	placeholders := make([]string, len(ids))
	args := make([]interface{}, 0, len(ids)+1)
	args = append(args, newLevel)
	for i, id := range ids {
		placeholders[i] = "?"
		args = append(args, id)
	}
	_, err := s.db.Exec(`UPDATE episodic_memories SET hierarchy_level = ? WHERE id IN (`+strings.Join(placeholders, ",")+`)`, args...)
	if err != nil {
		return fmt.Errorf("mark episodic hierarchy: %w", err)
	}
	return nil
}

// ResolvePendingEpisodicAction marks a pending follow-up as resolved.
func (s *SQLiteMemory) ResolvePendingEpisodicAction(id int64) error {
	if id <= 0 {
		return nil
	}
	_, err := s.db.Exec(`
		UPDATE episodic_memories
		SET action_status = 'resolved', resolved_at = CURRENT_TIMESTAMP
		WHERE id = ?
	`, id)
	if err != nil {
		return fmt.Errorf("resolve pending episodic action: %w", err)
	}
	return nil
}

// GetEpisodicMemoryStats returns additive diagnostics for the episodic layer.
func (s *SQLiteMemory) GetEpisodicMemoryStats(hoursWindow int, recentLimit int) (map[string]interface{}, error) {
	if hoursWindow <= 0 {
		hoursWindow = 72
	}
	if recentLimit <= 0 {
		recentLimit = 5
	}

	var totalCount int
	if err := s.db.QueryRow(`SELECT COUNT(*) FROM episodic_memories`).Scan(&totalCount); err != nil {
		return nil, fmt.Errorf("count episodic memories: %w", err)
	}

	var recentCount int
	if err := s.db.QueryRow(`SELECT COUNT(*) FROM episodic_memories WHERE datetime(created_at) >= datetime('now', '-' || ? || ' hours')`, hoursWindow).Scan(&recentCount); err != nil {
		return nil, fmt.Errorf("count recent episodic memories: %w", err)
	}

	rows, err := s.db.Query(`SELECT source, COUNT(*) FROM episodic_memories GROUP BY source ORDER BY COUNT(*) DESC`)
	if err != nil {
		return nil, fmt.Errorf("group episodic sources: %w", err)
	}
	defer rows.Close()

	bySource := make(map[string]int)
	for rows.Next() {
		var source string
		var count int
		if err := rows.Scan(&source, &count); err != nil {
			return nil, fmt.Errorf("scan episodic source stats: %w", err)
		}
		bySource[source] = count
	}

	recentCards, err := s.GetRecentEpisodicMemoryCards(hoursWindow, recentLimit)
	if err != nil {
		return nil, err
	}
	recent := make([]map[string]interface{}, 0, len(recentCards))
	for _, card := range recentCards {
		recent = append(recent, map[string]interface{}{
			"event_date":         card.EventDate,
			"title":              card.Title,
			"summary":            card.Summary,
			"source":             card.Source,
			"session_id":         card.SessionID,
			"hierarchy_level":    card.HierarchyLevel,
			"action_status":      card.ActionStatus,
			"trigger_query":      card.TriggerQuery,
			"participants":       card.Participants,
			"related_doc_ids":    card.RelatedDocIDs,
			"emotional_valence":  card.EmotionalValence,
			"prompt_line":        formatEpisodicCard(card),
			"has_related_memory": len(card.RelatedDocIDs) > 0,
		})
	}

	return map[string]interface{}{
		"total_count":  totalCount,
		"recent_count": recentCount,
		"hours_window": hoursWindow,
		"by_source":    bySource,
		"recent_cards": recent,
	}, nil
}

